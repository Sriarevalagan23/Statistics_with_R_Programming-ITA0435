
a = c(1, 2, 3, 4, 5, 6)
print(a)
print(paste("Class:", class(a)))
print(paste("Type:", typeof(a)))

# Character Vector
b = c("Apple", "Banana", "Orange")
print(b)
print(paste("Class:", class(b)))
print(paste("Type:", typeof(b)))

# Logical Vector
log_vec = c(TRUE, FALSE, TRUE, FALSE)
print(log_vec)
print(paste("Class:", class(log_vec)))
print(paste("Type:", typeof(log_vec)))